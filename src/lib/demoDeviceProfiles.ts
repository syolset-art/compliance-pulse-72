export async function seedDemoDevices(): Promise<number> {
  // Delete existing demo devices first
  await supabase.from("assets").delete().eq("asset_type", "hardware").filter("metadata->>is_demo_device", "eq", "true");

  const { data, error } = await supabase.from("assets").insert(demoDevices).select();
  if (error) throw error;
  return data?.length || 0;
}

export async function deleteDemoDevices(): Promise<number> {
  const { data, error } = await supabase
    .from("assets")
    .delete()
    .eq("asset_type", "hardware")
    .filter("metadata->>is_demo_device", "eq", "true")
    .select();
  if (error) throw error;
  return data?.length || 0;
}
